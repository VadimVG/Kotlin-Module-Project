open class WarningsText {

    fun notFoundNumber() {
        println("Номер не найден")
        println("Повторите попытку ввода числа")
    }

    fun notNumberInput() {
        println("Необходимо ввести число")
        println("Повторите попытку ввода")
    }

    fun archiveNameError() {
        println("Название архива должно быть заполнено")
        println("Повторите попытку ввода названия архива")
    }

    fun noteNameError() {
        println("Название и содержание заметки должны быть заполнены")
        println("Повторите попытку создания заметки")
    }
}